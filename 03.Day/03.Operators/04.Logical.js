// 4. Logical Operators
console.log("\nLogical Operators:");
let isRaining = true, isCold = false;
console.log("Logical AND:", isRaining && isCold); // false
console.log("Logical OR:", isRaining || isCold); // true
console.log("Logical NOT:", !isRaining);         // false

// Summary of Logical Operators
// && (AND): Returns true if both conditions are true.
// || (OR): Returns true if at least one condition is true.
// ! (NOT): Reverses the boolean value (true becomes false and vice versa).