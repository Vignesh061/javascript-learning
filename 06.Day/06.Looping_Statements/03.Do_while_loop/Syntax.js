/**
 * Do...while loop
 * In JavaScript, a do...while loop is a control flow statement
 * used to execute a block of code at least once, and then repeatedly as long as the specified condition evaluates to true.
 * The general syntax of a do...while loop in JavaScript is as follows:
 * (inlization)
 * do {
 *   code to be executed at least once and then repeatedly as long as the condition is true
 *   (iteration)
 * } while (condition);
 */

let a=1
do{
    console.log("Hey there")
    a++
}while(a<3)
