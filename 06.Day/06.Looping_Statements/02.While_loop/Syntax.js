/**
 * While loop
 * In JavaScript, a while loop is a control flow statement
 * used to repeatedly execute a block of code as long as the specified condition evaluates to true.
 * The general syntax of a while loop in JavaScript is as follows:
 * (initilization)
 * while (condition) {
 *   code to be executed as long as the condition is true
 *  (iteration)
 * }
 */

var i=1;
while(i<=5)
{
    console.log("Hello")
    i++
}
