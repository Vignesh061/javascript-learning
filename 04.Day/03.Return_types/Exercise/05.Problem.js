/**
 * Question: Write a function called `average` that takes an array of numbers as a parameter and returns the average of those numbers. 
 * The function should return the result using the return keyword. 
 * Provide an example of how to call this function and display the result.
 */


function average(a,b,c,d,e)
{
    return (a+b+c+d+e)/5
}
var avg=average(1,2,3,4,5)
console.log(avg)